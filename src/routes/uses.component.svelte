<h2 class="text-4xl font-bold my-4">What I Use</h2>
<h3 class="text-2xl font-bold my-2">Desktop Software</h3>
<p class="text-lg mb-2">
    You may
    <a
        class="link-accent"
        href="https://git.wxllow.dev/wxllow/dotfiles"
        target="_blank">view my dotfiles here</a
    >.
</p>
<ul class="list-disc space-y-2 mb-4">
    <li>
        <span class="font-bold">Operating System</span>: I primarily use
        <a class="link-accent" href="https://archlinux.org/" target="_blank"
            >Arch Linux</a
        >
        because of its customizability, bleeding-edge packages, and the
        <a class="link-accent" href="https://aur.archlinux.org/" target="_blank"
            >AUR</a
        >. I also recommend
        <a class="link-accent" href="https://endeavouros.com/" target="_blank"
            >EndeavourOS</a
        >, an easy to setup Arch-based distro! For server use, I recommend
        Fedora, Debian, or Ubuntu.
    </li>
    <li>
        <span class="font-bold">Window Manager</span>: I use
        <a class="link-accent" href="https://hyprland.org/" target="_blank"
            >Hyprland</a
        >, a beautiful, easily customizable Wayland window manager. You can find
        my configuration
        <a
            class="link-accent"
            href="https://git.wxllow.dev/wxllow/dotfiles"
            target="_blank">here</a
        >.
    </li>
    <li>
        <span class="font-bold">Terminal</span>:
        <a
            class="link-accent"
            href="https://sw.kovidgoyal.net/kitty/"
            target="_blank">Kitty</a
        >
    </li>
    <li>
        <span class="font-bold">Browser</span>: I use
        <a
            class="link-accent"
            href="https://github.com/ungoogled-software/ungoogled-chromium"
            target="_blank">Ungoogled Chromium</a
        >
        as it is a full-fledged Chromium browser, but without the Google spyware.
        I use a few extensions, notably
        <a class="link-accent" href="https://ublockorigin.com/" target="_blank"
            >uBlock Origin</a
        >,
        <a
            class="link-accent"
            href="https://chrome.google.com/webstore/detail/decentraleyes/ldpochfccmkkmhdbclfhpagapcfdljkj"
            target="_blank">Decentraleyes</a
        >,
        <a class="link-accent" href="https://vimium.github.io/" target="_blank"
            >Vimium</a
        >, and
        <a class="link-accent" href="https://privacybadger.org/" target="_blank"
            >Privacy Badger</a
        >
    </li>
    <li>
        <span class="font-bold">Text/Code Editor</span>: I use
        <a
            class="link-accent"
            href="https://code.visualstudio.com/"
            target="_blank">VSCode</a
        >
        (
        <a class="link-accent" href="https://vscodium.com/" target="_blank"
            >VSCodium</a
        >
        ) and
        <a class="link-accent" href="https://neovim.io/" target="_blank"
            >Neovim</a
        >
        as my editors of choice. For neovim, I use a configuration based on
        <a
            class="link-accent"
            href="https://github.com/nvim-lua/kickstart.nvim"
            target="_blank">kickstart.nvim</a
        >. On both editors, I use the
        <a class="link-accent" href="https://codeium.com/" target="_blank"
            >Codeium</a
        > AI code completion plugin, it is a free alternative to Github Copilot which
        is only trained on permissive code
    </li>
    <li>
        <span class="font-bold">File Manager</span>: Dolphin
    </li>
    <li>
        <span class="font-bold">Image Editors</span>:
        <a class="link-accent" href="https://krita.org/" target="_blank"
            >Krita</a
        >
        for digital drawing,
        <a class="link-accent" href="https://inkscape.org/" target="_blank"
            >Inkscape</a
        > for vector graphics
    </li>
    <li>
        <span class="font-bold">Video/audio player</span>:
        <a class="link-accent" href="https://mpv.io/" target="_blank">mpv</a>
    </li>
</ul>
